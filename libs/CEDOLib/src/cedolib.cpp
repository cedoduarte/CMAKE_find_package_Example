#include "cedolib.h"

CEDOLib::CEDOLib()
{
    // nothing to do here
}

CEDOLib::~CEDOLib()
{
    // nothing to do here
}

double CEDOLib::addition(double a, double b) const
{
    return a + b;
}

double CEDOLib::substraction(double a, double b) const
{
    return a - b;
}

double CEDOLib::multiplication(double a, double b) const
{
    return a * b;
}

double CEDOLib::division(double a, double b) const
{
    return a / b;
}
